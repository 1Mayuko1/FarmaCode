export const binaryDecode = (data, codedLetters) => {
    let numbersArr = []
    codedLetters.map(el => {
        return Object.keys(code39Symbols).map(valueX => {
            if (valueX === el.toString()) {
                return Object.keys(data).map(valueY => {
                    if (valueY === code39Symbols[valueX].toString()) {
                        return numbersArr.push(data[valueY])
                    } else return null
                })
            } else return null
        })
    })

    return [data['*'], ...numbersArr, data['*']]
}
